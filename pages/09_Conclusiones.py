st.markdown('En conclusión, Latinoamérica ha logrado ganar vida humana a costa de perder biodiversidad. Un desafío para el futuro no es solo mantener el crecimiento de los indicadores de salud, sino descifrar cómo prosperar y mejorar la calidad de vida sin sacrificar los ecosistemas que sustentan la vida a largo plazo. Vemos necesario cambiar ese paradigma mundial de "desarrollo a cualquier costo" hacia uno más regenerativo y menos letal para los ecosistemas de nuestro mundo.')
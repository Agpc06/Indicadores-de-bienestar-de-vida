st.markdown('La región de América Latina ha experimentado transformaciones profundas y exitosas en términos de bienestar. Se evidencia una mejora masiva en la esperanza de vida y una drástica reducción de la mortalidad infantil. Estos logros son el resultado directo de la urbanización, la industrialización y la mejora de los sistemas de salud pública.')

